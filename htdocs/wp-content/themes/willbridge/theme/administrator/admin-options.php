				stristr($themeicon, '.png') !== false || 
				stristr($themeicon, '.jpg') !== false || 
				stristr($themeicon, '.jpeg') !== false || 
				stristr($themeicon, '.gif') !== false || 
				stristr($themeicon, '.ico') !== false 
			) { 
                $themeicons[] = $themeicon;
            }
        }
    }
}


function get_theme_bgs() {
	global $wpdb, $shortname;
	
	$theme_bgs = $wpdb->get_results('SELECT * FROM ' . $wpdb->prefix . $shortname . '_bgs');
	
	return $theme_bgs;
}


function get_theme_fonts() {
    global $wpdb, $shortname;

    $theme_fonts = $wpdb->get_results('SELECT * FROM ' . $wpdb->prefix . $shortname . '_fonts');

    return $theme_fonts;
}


function get_theme_icons() {
    global $wpdb, $shortname;

    $theme_icons = $wpdb->get_results('SELECT * FROM ' . $wpdb->prefix . $shortname . '_icons');

    return $theme_icons;
}


